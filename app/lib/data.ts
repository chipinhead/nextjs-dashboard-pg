// Create a new pool
const pool = new Pool({
  connectionString: process.env.POSTGRES_URL,
});

async function query<T>(text: string, params?: any[]): Promise<QueryResult<T>> {
  const client = await pool.connect();
  try {
    return await client.query<T>(text, params);
  } finally {
    client.release();
  }
}

